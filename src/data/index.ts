// ============ HELPER FUNCTIONS ============
export function getToolBySlug(slug: string): Tool | undefined {
  return tools.find(t => t.slug === slug);
}

export function getToolsByCategory(categorySlug: string): Tool[] {
  return tools.filter(t => t.category === categorySlug);
}

export function getCategoryBySlug(slug: string): Category | undefined {
  return categories.find(c => c.slug === slug);
}

export function getFeaturedTools(): Tool[] {
  return tools.filter(t => t.featured);
}

export function searchTools(query: string, filters?: { category?: string; pricing?: string }): Tool[] {
  let results = tools;
  
  if (query) {
    const q = query.toLowerCase();
    results = results.filter(t =>
      t.name.toLowerCase().includes(q) ||
      t.tagline.toLowerCase().includes(q) ||
      t.description.toLowerCase().includes(q) ||
      t.tags.some(tag => tag.toLowerCase().includes(q)) ||
      t.category.toLowerCase().includes(q)
    );
  }
  
  if (filters?.category) {
    results = results.filter(t => t.category === filters.category);
  }
  
  if (filters?.pricing) {
    results = results.filter(t => t.pricing.model === filters.pricing);
  }
  
  return results;
}

export function getBlogPostBySlug(slug: string): BlogPost | undefined {
  return blogPosts.find(p => p.slug === slug);
}

export function getRelatedTools(tool: Tool, limit = 4): Tool[] {
  return tools
    .filter(t => t.slug !== tool.slug)
    .map(t => ({
      tool: t,
      score: (t.category === tool.category ? 3 : 0) +
        t.tags.filter(tag => tool.tags.includes(tag)).length
    }))
    .sort((a, b) => b.score - a.score)
    .slice(0, limit)
    .map(item => item.tool);
}
